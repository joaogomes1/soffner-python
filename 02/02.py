# 2. Idem, testando se é menor, igual ou maior que zero.

userInput = int(input("Informe um número inteiro: "))

if ( userInput > 0 ):
    print("O número informado é maior que zero.")
elif (userInput) < 0:
    print("O número informado é menor que zero.")
else:
    print("O número informado é zero.")

print('\n' + "[fim do programa]")
