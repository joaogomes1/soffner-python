# 9. Escreva um programa que receba dois números e mostre a soma, a subtração, a multiplicação e a divisão desses números.

a = float(input("Informe um número: "))
b = float(input("Informe outro número: "))

print("soma: " + str(a + b))
print("subtração: " + str(a - b))
print("multiplicação: " + str(a * b))
print("divisão: " + str(a / b))
