# 1. Escreva um programa que recebe um número inteiro e testa se o mesmo é igual a zero.

userInput = int(input("Informe um número inteiro: "))

# print("Número informado: " + userInput )

if ( userInput == 0 ):
    print("O número informado é zero.")
else:
    print("O número informado não é zero.")

